/*

     Operadores aritméticos
+ soma, concatena/soma (ação dupla)
- subtrai
* multiplica
/ divide 


     Operadores lógicos - retornam um valor booleano (true/falso)
> Maior que
< Menor que
>= Maior ou igual
<= Menor ou igual
== Iugal a
=== Identico a (igual e do mesmo tipo de dados)
!== Não identico a (diferente: tipo e valor e do mesmo tipo de dado)

*/

let numero1 = parseInt("34");
let numero2 = parseFloat("33");

console.log("Operadores aritmeticos");
console.log(numero1 + numero2);
console.log(numero1 - numero2);
console.log(numero1 * numero2);
console.log(numero1 / numero2);

console.log("Operadores lógicos");
console.log(numero1 < numero2);
console.log(numero1 > numero2);
console.log(numero1 <= numero2);
console.log(numero1 <= numero2);
console.log(numero1 == numero2);
console.log(numero1 === numero2);